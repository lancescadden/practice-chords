def render_big_text(text: str) -> list[str]:
    """Render text as big ASCII art. Returns list of 6 lines."""
    lines = ["", "", "", "", "", ""]

    for char in text:
        letter = LETTERS.get(char, LETTERS.get(' '))
        if letter:
            for i in range(6):
                lines[i] += letter[i] + " "

    return lines


def get_big_chord(chord_name: str) -> str:
    """Get big ASCII art for a chord name."""
    lines = render_big_text(chord_name)
    return "\n".join(lines)


